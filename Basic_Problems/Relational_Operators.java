public class Relational_Operators {
    public static void main(String args[]){
        int a = 5;
        int b = 4;

        System.out.println(a==b); // equals to (output-false)
        System.out.println(a!=b); // not equals to (output-true)
        System.out.println(a>b); // greater than (output-true)
        System.out.println(a<b); // less than (output-false)
        System.out.println(a>=b); // greater than equals to (output-true)
        System.out.println(a<=b); // less than equals to (output-false)

    }
}