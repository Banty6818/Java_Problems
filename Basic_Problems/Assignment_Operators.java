public class Assignment_Operators {
    public static void main(String args[]){
        int a = 10; // = assigns a value to a variable
        System.out.println("A:"+a);
        a += 10; // (a+=10) is (a = a+10)
        System.out.println("A:"+a);
        a -= 10; // (a-=10) is (a = a-10)
        System.out.println("A:"+a);
        a /= 2; // (a/=2) is (a = a/2)
        System.out.println("A:"+a);
        a *= 2; // (a*=2) is (a = a*2)
        System.out.println("A:"+a);
    }
}
