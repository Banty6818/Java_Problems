public class Loops {
    public static void main(String[] args) {
        // while loop
        int x = 1;
        while(x>=10){
            System.out.print(x+" ");
            x++;
        }
        System.out.println();

        // for loop
        for(int i=1; i<=10; i++){
            System.out.println(x+" ");
        }
        System.out.println();

        
    }
}
